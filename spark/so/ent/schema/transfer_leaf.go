// Edges are the edges for the TransferLeaf table.
func (TransferLeaf) Edges() []ent.Edge {
	return []ent.Edge{
		edge.To("transfer", Transfer.Type).
			Unique().
			Immutable().
			Required(),
		edge.To("leaf", TreeNode.Type).
			Unique().
			Immutable().
			Required(),
		edge.To("transfer_receiver", TransferReceiver.Type).
			Unique().
			Field("transfer_receiver_id"),
		edge.To("transfer_sender", TransferSender.Type).
			Unique().
			Field("transfer_sender_id"),
	}
}

