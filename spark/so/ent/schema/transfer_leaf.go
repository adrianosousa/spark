func (TransferLeaf) Hooks() []ent.Hook {
	return []ent.Hook{
		// Validate that the network in the tree matches the network in the transfer
		hook.On(
			func(next ent.Mutator) ent.Mutator {
				return hook.TransferLeafFunc(func(ctx context.Context, m *gen.TransferLeafMutation) (ent.Value, error) {
					transferID, transferExists := m.TransferID()
					leafID, leafExists := m.LeafID()

					if !transferExists || !leafExists {
						return nil, fmt.Errorf("transfer leaf mutation must contain transfer ID and leaf ID")
					}

					client := m.Client()

					transfer, err := client.Transfer.Get(ctx, transferID)
					if err != nil {
						return nil, fmt.Errorf("failed to fetch transfer: %w", err)
					}

					leaf, err := client.TreeNode.Get(ctx, leafID)
					if err != nil {
						return nil, fmt.Errorf("failed to fetch leaf: %w", err)
					}

					if transfer.Network != leaf.Network {
						return nil, fmt.Errorf("network mismatch: transfer network %s does not match leaf network %s", transfer.Network, leaf.Network)
					}

					return next.Mutate(ctx, m)
				})
			},
			ent.OpCreate,
		),
		// Define helper hooks that will populate TXIDs for the transactions in the mutation.
		// Clearing a field will also clear a corresponding TXID.
		// Be mindful that updates on transactions are slower because the DB indexes on TXIDs are also updated.
		// The first hook handles single entity updates and the second hook handles creations.
		hook.On(
			func(next ent.Mutator) ent.Mutator {
				return hook.TransferLeafFunc(func(ctx context.Context, m *gen.TransferLeafMutation) (ent.Value, error) {
					err := checkTransferLeafTxids(m)
					if err != nil {
						return nil, err
					}

					leafIdUuid, _ := m.ID()
					leafId := leafIdUuid.String()

					// Handling an update on a field
					// intermediate_refund_tx is NotEmpty() so it cannot be cleared, only updated
					if intermediateRefundTxBytes, ok := m.IntermediateRefundTx(); ok {
						intermediateRefundTx, err := common.TxFromRawTxBytes(intermediateRefundTxBytes)
						if err != nil {
							return nil, fmt.Errorf("failed to parse intermediate_refund_tx for transfer leaf %s: %w", leafId, err)
						}
						intermediateRefundTxid := intermediateRefundTx.TxHash()
						m.SetIntermediateRefundTxid(st.NewTxID(intermediateRefundTxid))

						timelock, err := bitcointransaction.GetAndValidateUserTimelock(intermediateRefundTxBytes)
						if err != nil {
							return nil, fmt.Errorf("failed to parse intermediate_refund_tx timelock for transfer leaf %s: %w", leafId, err)
						}
						m.SetIntermediateRefundTimelock(uint64(timelock))
					}

					if m.IntermediateDirectRefundTxCleared() {
						m.ClearIntermediateDirectRefundTxid()
						m.ClearIntermediateDirectRefundTimelock()
					} else {
						if intermediateDirectRefundTxBytes, ok := m.IntermediateDirectRefundTx(); ok {
							if intermediateDirectRefundTxBytes == nil {
								m.ClearIntermediateDirectRefundTxid()
								m.ClearIntermediateDirectRefundTimelock()
							} else {
								intermediateDirectRefundTx, err := common.TxFromRawTxBytes(intermediateDirectRefundTxBytes)
								if err != nil {
									return nil, fmt.Errorf("failed to parse intermediate_direct_refund_tx for transfer leaf %s: %w", leafId, err)
								}
								intermediateDirectRefundTxid := intermediateDirectRefundTx.TxHash()
								m.SetIntermediateDirectRefundTxid(st.NewTxID(intermediateDirectRefundTxid))

								timelock, err := bitcointransaction.GetAndValidateUserTimelock(intermediateDirectRefundTxBytes)
								if err != nil {
									return nil, fmt.Errorf("failed to parse intermediate_direct_refund_tx timelock for transfer leaf %s: %w", leafId, err)
								}
								m.SetIntermediateDirectRefundTimelock(uint64(timelock))
							}
						}
					}

					if m.IntermediateDirectFromCpfpRefundTxCleared() {
						m.ClearIntermediateDirectFromCpfpRefundTxid()
						m.ClearIntermediateDirectFromCpfpRefundTimelock()
					} else {
						if intermediateDirectFromCpfpRefundTxBytes, ok := m.IntermediateDirectFromCpfpRefundTx(); ok {
							if intermediateDirectFromCpfpRefundTxBytes == nil {
								m.ClearIntermediateDirectFromCpfpRefundTxid()
								m.ClearIntermediateDirectFromCpfpRefundTimelock()
							} else {
								intermediateDirectFromCpfpRefundTx, err := common.TxFromRawTxBytes(intermediateDirectFromCpfpRefundTxBytes)
								if err != nil {
									return nil, fmt.Errorf("failed to parse intermediate_direct_from_cpfp_refund_tx for transfer leaf %s: %w", leafId, err)
								}
								intermediateDirectFromCpfpRefundTxid := intermediateDirectFromCpfpRefundTx.TxHash()
								m.SetIntermediateDirectFromCpfpRefundTxid(st.NewTxID(intermediateDirectFromCpfpRefundTxid))

								timelock, err := bitcointransaction.GetAndValidateUserTimelock(intermediateDirectFromCpfpRefundTxBytes)
								if err != nil {
									return nil, fmt.Errorf("failed to parse intermediate_direct_from_cpfp_refund_tx timelock for transfer leaf %s: %w", leafId, err)
								}
								m.SetIntermediateDirectFromCpfpRefundTimelock(uint64(timelock))
							}
						}
					}
					return next.Mutate(ctx, m)
				})
			},
			ent.OpUpdateOne|ent.OpUpdate,
		),
		// Create ent hook
		hook.On(
			func(next ent.Mutator) ent.Mutator {
				return hook.TransferLeafFunc(func(ctx context.Context, m *gen.TransferLeafMutation) (ent.Value, error) {
					err := checkTransferLeafTxids(m)
					if err != nil {
						return nil, err
					}

					leafIdUuid, _ := m.ID()
					leafId := leafIdUuid.String()

					intermediateRefundTxBytes, _ := m.IntermediateRefundTx()
					intermediateRefundTx, err := common.TxFromRawTxBytes(intermediateRefundTxBytes)
					if err != nil {
						return nil, fmt.Errorf("failed to parse intermediate_refund_tx for transfer leaf %s: %w", leafId, err)
					}
					intermediateRefundTxid := intermediateRefundTx.TxHash()
					m.SetIntermediateRefundTxid(st.NewTxID(intermediateRefundTxid))

					timelock, err := bitcointransaction.GetAndValidateUserTimelock(intermediateRefundTxBytes)
					if err != nil {
						return nil, fmt.Errorf("failed to parse intermediate_refund_tx timelock for transfer leaf %s: %w", leafId, err)
					}
					m.SetIntermediateRefundTimelock(uint64(timelock))

					intermediateDirectRefundTxBytes, _ := m.IntermediateDirectRefundTx()
					if intermediateDirectRefundTxBytes != nil {
						intermediateDirectRefundTx, err := common.TxFromRawTxBytes(intermediateDirectRefundTxBytes)
						if err != nil {
							return nil, fmt.Errorf("failed to parse intermediate_direct_refund_tx for transfer leaf %s: %w", leafId, err)
						}
						intermediateDirectRefundTxid := intermediateDirectRefundTx.TxHash()
						m.SetIntermediateDirectRefundTxid(st.NewTxID(intermediateDirectRefundTxid))

						timelock, err := bitcointransaction.GetAndValidateUserTimelock(intermediateDirectRefundTxBytes)
						if err != nil {
							return nil, fmt.Errorf("failed to parse intermediate_direct_refund_tx timelock for transfer leaf %s: %w", leafId, err)
						}
						m.SetIntermediateDirectRefundTimelock(uint64(timelock))
					}

					intermediateDirectFromCpfpRefundTxBytes, _ := m.IntermediateDirectFromCpfpRefundTx()
					if intermediateDirectFromCpfpRefundTxBytes != nil {
						intermediateDirectFromCpfpRefundTx, err := common.TxFromRawTxBytes(intermediateDirectFromCpfpRefundTxBytes)
						if err != nil {
							return nil, fmt.Errorf("failed to parse intermediate_direct_from_cpfp_refund_tx for transfer leaf %s: %w", leafId, err)
						}
						intermediateDirectFromCpfpRefundTxid := intermediateDirectFromCpfpRefundTx.TxHash()
						m.SetIntermediateDirectFromCpfpRefundTxid(st.NewTxID(intermediateDirectFromCpfpRefundTxid))

						timelock, err := bitcointransaction.GetAndValidateUserTimelock(intermediateDirectFromCpfpRefundTxBytes)
						if err != nil {
							return nil, fmt.Errorf("failed to parse intermediate_direct_from_cpfp_refund_tx timelock for transfer leaf %s: %w", leafId, err)
						}
						m.SetIntermediateDirectFromCpfpRefundTimelock(uint64(timelock))
					}
					return next.Mutate(ctx, m)
				})
			},
			ent.OpCreate,
		),
	}
}

// Checks if the txids and timelocks are set directly.
// This is not allowed because the txids and timelocks are generated by hooks from the transactions.
func checkTransferLeafTxids(m *gen.TransferLeafMutation) error {
	if _, ok := m.IntermediateRefundTxid(); ok {
		return fmt.Errorf("intermediate_refund_txid is not allowed to be set directly")
	}
	if _, ok := m.IntermediateDirectRefundTxid(); ok {
		return fmt.Errorf("intermediate_direct_refund_txid is not allowed to be set directly")
	}
	if _, ok := m.IntermediateDirectFromCpfpRefundTxid(); ok {
		return fmt.Errorf("intermediate_direct_from_cpfp_refund_txid is not allowed to be set directly")
	}
	if _, ok := m.IntermediateRefundTimelock(); ok {
		return fmt.Errorf("intermediate_refund_timelock is not allowed to be set directly")
	}
	if _, ok := m.IntermediateDirectRefundTimelock(); ok {
		return fmt.Errorf("intermediate_direct_refund_timelock is not allowed to be set directly")
	}
	if _, ok := m.IntermediateDirectFromCpfpRefundTimelock(); ok {
		return fmt.Errorf("intermediate_direct_from_cpfp_refund_timelock is not allowed to be set directly")
	}
	return nil
}
